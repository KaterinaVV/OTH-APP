/**
 * RFC 7807 compliance error.
 * @param type A URI reference that identifies the problem type.
 * @param title A short, human-readable summary of the problem type.
 * @param status The HTTP status code generated by the origin server for this occurrence of the problem.
 * @param detail A human-readable explanation specific to this occurrence of the problem.
 */
data class Error(

    @field:JsonProperty("type") val type: kotlin.String? = null,

    @field:JsonProperty("title") val title: kotlin.String? = null,

    @field:JsonProperty("status") val status: kotlin.Int? = null,

    @field:JsonProperty("detail") val detail: kotlin.String? = null
) {

}
